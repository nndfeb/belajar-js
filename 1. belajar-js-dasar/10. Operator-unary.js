/**
 * ? Operator Unary
 *
 **     Operator     Keterangan
 *      +           Menandakan nilai positif
 *      -           Menandakan nilai negatif
 *      ++          increment menaikan satu angka
 *      --          decrement menurunkan satu angka
 */

let nilaiA = +1;
console.log(nilaiA);

nilaiA--;
console.log(nilaiA);

nilaiA++;
console.log(nilaiA);

nilaiA = -nilaiA;
console.log(nilaiA);
