/**
 * ?Operator perbandingan
 * berfungsi unutk membandingkan duabuah data atau lebih
 * result dari perbandingan adalah boolean (true atau false)
 *
 **  Operator    |   Keterangan
 *  >               Lebih dari
 *  <               Kurang dari
 *  >=              Lebih dari sama dengan
 *  <=              Kurang dari sama dengan
 *  ==              Sama dengan
 *  ===             Sama dengan dan typenya sama
 *  !=              Tidak sama dengan
 *  !==             Tidak sama dengan dan typenya beda
 *
 */

let nilaiA = 5 == "5";
console.log(nilaiA);

nilaiA = 5 === "5";
console.log(nilaiA);

nilaiA = 5 > 10;
console.log(nilaiA);

nilaiA = 5 < 10;
console.log(nilaiA);

nilaiA = 10 < 10;
console.log(nilaiA);

nilaiA = 10 <= 10;
console.log(nilaiA);
