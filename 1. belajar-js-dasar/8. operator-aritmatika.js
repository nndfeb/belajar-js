/**
 * ? Operator Aritmatika
 *
 * Operator     Keterangan
 *  +   =>      Penambahan
 *  -   =>      Pengurangan
 *  *   =>      Perkalian
 *  **   =>     Exponensial
 *  /   =>      Pembagian
 *  %   =>      Sisa bagi
 */

let nilaiA = 1 + 2;
console.log(`Nilai dari 1 + 2 = ${nilaiA}`); // Output : Nilai dari 1 + 2 = 3

nilaiA = nilaiA - 1;
console.log(nilaiA); // Output : 2
nilaiA = nilaiA * 2;
console.log(nilaiA); // Output : 4
let nilaiB = nilaiA;
console.log(nilaiB); // Output : 4
