/**
 *?  Boolean
 *  Boolean merupakan wapper class untuk tipe primitif boolean
 *  Boolean memiliki method toString() unutk mengkonversi ke string
 *  dan memiliki method valueOf() untuk mengkonversi ke tipe boolean primitif
 *  https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Boolean
 */

const boolean = Boolean(true);
console.log(boolean); // Output : true
console.log(typeof boolean); // Output : boolean
console.log(boolean.toString()); // Output : true
console.log(boolean.valueOf()); // Output : true
